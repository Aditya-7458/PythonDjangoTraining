# Conditional statement perform the control flow in the code 

'''
There are three type of conditional statement 
1. if
2. elif
3. else 

first execute if then elif and then else 



'''
a = 10
b = 90
c = 45

if a >= b and a >= c:
    print(a)
elif b >= a and b >= c:
    print(b)
else:
    print(c)
